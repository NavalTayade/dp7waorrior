package com.loops;



public class DivBy
{
	public static void main (String args[])
	{
		
			for (int i=1;i<=50;i++)
			
			if(i%3==0 && i%5==0)
		{
			
			System.out.println("hello.");
			
		}
			else if(i%5==0)
			{
				System.out.println("wellcome");

			}
			else if(i%3==0)
			{
				System.out.println("bye");

			}
			else
			{
				System.out.println(i);

			}
	}
	
}
